# Data Insights table builder
# ---------------------------------------------------------------------------

def _build_insights_table(df: pd.DataFrame, body_style, header_style, secondary_color) -> list:
    """
    Build a Data Insights table showing per-column statistics.
    Returns a list of flowables (may be empty if no numeric cols).
    """
    flowables = []

    numeric_cols = df.select_dtypes(include=["number"]).columns.tolist()
    cat_cols     = df.select_dtypes(include=["object", "category"]).columns.tolist()

    # ── Numeric stats table ──────────────────────────────────────────────────
    if numeric_cols:
        header_row = [
            Paragraph("<b>Column</b>", header_style),
            Paragraph("<b>Min</b>",    header_style),
            Paragraph("<b>Max</b>",    header_style),
            Paragraph("<b>Mean</b>",   header_style),
            Paragraph("<b>Median</b>", header_style),
            Paragraph("<b>Std Dev</b>",header_style),
            Paragraph("<b>Missing%</b>", header_style),
        ]
        rows = [header_row]

        for col in numeric_cols[:20]:   # cap at 20 columns
            s      = df[col]
            miss   = round(s.isnull().sum() / max(len(df), 1) * 100, 1)
            s_num  = s.dropna()
            row = [
                Paragraph(str(col), body_style),
                Paragraph(f"{s_num.min():.4g}" if not s_num.empty else "—", body_style),
                Paragraph(f"{s_num.max():.4g}" if not s_num.empty else "—", body_style),
                Paragraph(f"{s_num.mean():.4g}" if not s_num.empty else "—", body_style),
                Paragraph(f"{s_num.median():.4g}" if not s_num.empty else "—", body_style),
                Paragraph(f"{s_num.std():.4g}" if len(s_num) > 1 else "—", body_style),
                Paragraph(f"{miss}%", body_style),
            ]
            rows.append(row)

        tbl = Table(rows, colWidths=[120, 55, 55, 55, 55, 55, 55])
        tbl.setStyle(TableStyle([
            ("BACKGROUND",    (0, 0), (-1, 0),  colors.HexColor("#f1f5f9")),
            ("LINEBELOW",     (0, 0), (-1, 0),  1.2, secondary_color),
            ("BOX",           (0, 0), (-1, -1), 0.5, colors.HexColor("#cbd5e1")),
            ("INNERGRID",     (0, 0), (-1, -1), 0.3, colors.HexColor("#e2e8f0")),
            ("TOPPADDING",    (0, 0), (-1, -1), 5),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
            ("LEFTPADDING",   (0, 0), (-1, -1), 6),
            ("RIGHTPADDING",  (0, 0), (-1, -1), 6),
            ("VALIGN",        (0, 0), (-1, -1), "MIDDLE"),
            ("ROWBACKGROUNDS",(0, 1), (-1, -1), [colors.white, colors.HexColor("#f8fafc")]),
        ]))
        flowables.append(tbl)
        flowables.append(Spacer(1, 8))

    # ── Categorical top-values table ─────────────────────────────────────────
    if cat_cols:
        cat_header = [
            Paragraph("<b>Column</b>",     header_style),
            Paragraph("<b>Top Values (count)</b>", header_style),
            Paragraph("<b>Unique</b>",     header_style),
            Paragraph("<b>Missing%</b>",   header_style),
        ]
        cat_rows = [cat_header]

        for col in cat_cols[:10]:
            s     = df[col]
            miss  = round(s.isnull().sum() / max(len(df), 1) * 100, 1)
            unique = s.nunique()
            top3  = s.value_counts().head(3)
            top_str = ", ".join(f"{v}({c})" for v, c in top3.items()) if not top3.empty else "—"
            cat_rows.append([
                Paragraph(str(col), body_style),
                Paragraph(top_str[:80], body_style),
                Paragraph(str(unique), body_style),
                Paragraph(f"{miss}%", body_style),
            ])

        cat_tbl = Table(cat_rows, colWidths=[100, 280, 55, 65])
        cat_tbl.setStyle(TableStyle([
            ("BACKGROUND",    (0, 0), (-1, 0),  colors.HexColor("#f1f5f9")),
            ("LINEBELOW",     (0, 0), (-1, 0),  1.2, secondary_color),
            ("BOX",           (0, 0), (-1, -1), 0.5, colors.HexColor("#cbd5e1")),
            ("INNERGRID",     (0, 0), (-1, -1), 0.3, colors.HexColor("#e2e8f0")),
            ("TOPPADDING",    (0, 0), (-1, -1), 5),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
            ("LEFTPADDING",   (0, 0), (-1, -1), 6),
            ("RIGHTPADDING",  (0, 0), (-1, -1), 6),
            ("VALIGN",        (0, 0), (-1, -1), "MIDDLE"),
            ("ROWBACKGROUNDS",(0, 1), (-1, -1), [colors.white, colors.HexColor("#f8fafc")]),
        ]))
        flowables.append(cat_tbl)

    return flowables


