# ---------------------------------------------------------------------------
# Two-pass Canvas — Page X of Y + Corporate Rules
# ---------------------------------------------------------------------------

class NumberedCanvas(canvas.Canvas):
    """Two-pass canvas for dynamic page count and corporate header/footer rules."""

    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        num_pages = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self.draw_page_decorations(num_pages)
            super().showPage()
        super().save()

    def draw_page_decorations(self, page_count):
        self.saveState()
        self.setFont("Helvetica-Bold", 8)
        self.setFillColor(colors.HexColor("#475569"))
        self.setStrokeColor(colors.HexColor("#cbd5e1"))
        self.setLineWidth(0.5)

        if self._pageNumber > 1:
            self.line(54, 735, 558, 735)
            self.drawString(54, 742, "EXECUTIVE BUSINESS INTELLIGENCE REPORT")
            self.drawRightString(558, 742, "CONFIDENTIAL")

        self.line(54, 60, 558, 60)
        self.setFont("Helvetica", 8)
        self.drawString(54, 45, "Generated by Agentic Data Analyst System")
        self.drawRightString(558, 45, f"Page {self._pageNumber} of {page_count}")
        self.restoreState()


# ---------------------------------------------------------------------------
# Markdown → ReportLab HTML
# ---------------------------------------------------------------------------

def _md_to_html(text: str) -> str:
    if not text:
        return ""
    text = re.sub(r"\*\*(.*?)\*\*", r"<b>\1</b>", text)
    text = re.sub(r"\*(.*?)\*",     r"<i>\1</i>", text)
    text = (
        text.replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
    )
    text = (
        text.replace("&lt;b&gt;",  "<b>").replace("&lt;/b&gt;", "</b>")
            .replace("&lt;i&gt;",  "<i>").replace("&lt;/i&gt;", "</i>")
    )
    return text.strip()


